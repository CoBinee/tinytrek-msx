; sin の値を取得する
;
_MathGetSin::
    
    ; レジスタの保存
    push    hl

    ; a  < 角度
    ; de > sin 値
    
    ; 値の取得
    ld      d, #0x00
    add     a, a
    rl      d
    ld      e, a
    ld      hl, #trigonometricSinTable
    add     hl, de
    ld      e, (hl)
    inc     hl
    ld      d, (hl)
    
    ; レジスタの復帰
    pop     hl
    
    ; 終了
    ret

; cos の値を取得する
;
_MathGetCos::
    
    ; レジスタの保存
    push    hl
    
    ; a  < 角度
    ; de > cos 値
    
    ; 値の取得
    add     a, #0x40
    ld      d, #0x00
    add     a, a
    rl      d
    ld      e, a
    ld      hl, #trigonometricSinTable
    add     hl, de
    ld      e, (hl)
    inc     hl
    ld      d, (hl)
    
    ; レジスタの復帰
    pop     hl
    
    ; 終了
    ret

; atan2 の値を取得する
;
_MathGetAtan2::
    
    ; レジスタの保存
    push    hl
    push    bc
    push    de
    
    ; hl < Y/X 値
    ; a  > 角度
    
    ; 値の取得
    ld      c, #0x00
    ld      a, h
    sla     h
    rl      c
    bit     #0x00, c
    jr      z, 10$
    neg
    jp      po, 10$
    ld      a, #0x7f
10$:
    ld      h, a
    ld      a, l
    sla     l
    rl      c
    bit     #0x00, c
    jr      z, 11$
    neg
    jp      po, 11$
    ld      a, #0x7f
11$:
    ld      l, a
    cp      h
    jr      nc, 12$
    ld      a, h
12$:
    cp      #0x10
    jr      c, 13$
    sra     a
    sra     l
    sra     h
    jr      12$
13$:
    ld      a, h
    add     a, a
    add     a, a
    add     a, a
    add     a, a
    add     a, l
    ld      e, a
    ld      d, #0x00
    ld      hl, #trigonometricAtanAngleTable
    add     hl, de
    ld      a, (hl)
    ld      b, c
    inc     b
    bit     #0x01, b
    jr      z, 14$
    neg
14$:
    ld      b, #0x00
    ld      hl, #trigonometricAtanOffsetTable
    add     hl, bc
    add     a, (hl)
    
    ; レジスタの復帰
    pop     de
    pop     bc
    pop     hl
    
    ; 終了
    ret

